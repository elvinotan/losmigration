	public static boolean isIgnore(String filename) {
		if (ignoredlist.isEmpty()) {
			for (String f : ignoredFile) { ignoredlist.add(f); }
			for (String f : unOpenedFile) { ignoredlist.add(f); }
//			for (String f : unStandardFile) { ignoredlist.add(f); }
			
		}
		
		return ignoredlist.contains(filename);
	}
	
	public boolean isUnstandardFile(String filename) {
		for (String s : unStandardFile) {
			if (s.equals(filename)) { return true; }
		}
		
		return false;
	}
	
